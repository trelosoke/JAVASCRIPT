'use strict';
//Teste 0 == false e 0 === false, mostrando os resultados.

console.log(0 == false); //true, na igualdade comum, só é avalidado se o valor é igual.
console.log(0 === false); //false, na igualdade estrita, é avalidado se é o mesmo valor e o mesmo TIPO. No caso, 0 é number, false é boolean.