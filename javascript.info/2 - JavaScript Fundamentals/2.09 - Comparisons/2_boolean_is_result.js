'use strict';
//Todos os operadores de comparação retornam um valor boolean ("true" ou "false"):
console.log(2 > 1); //true
console.log(2 == 1); //false
console.log(2 != 1); //true

//O resultado de uma comparação pode ser atribuído a uma variável, assim como outros valores:
let result = 5 > 4;
console.log(result); //true