'use strict';
//Alterando a capitalização (caixa alta / caixa baixa)
    //Os métodos '.toLowerCase()' e '.toUpperCase()' alteram a "caixa" do texto:
        console.log('Interface'.toUpperCase()); //INTERFACE - caixa alta
        console.log('Interface'.toLowerCase()); //interface - caixa baixa

    //Ou se quisermos um único caractere de caixa baixa:
        console.log('Interface'[0].toLowerCase()); //i